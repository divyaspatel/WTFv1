/** Returns states for a given country code, or null if none defined */
export function getStates(countryCode) {
  return STATES_BY_COUNTRY[countryCode] || null;
}

/** Returns city list for a state code (or country code as fallback), or null */
export function getCities(countryCode, stateCode) {
  if (stateCode && CITIES_BY_STATE[stateCode]) return CITIES_BY_STATE[stateCode];
  if (CITIES_BY_COUNTRY[countryCode]) return CITIES_BY_COUNTRY[countryCode];
  return null;
}
